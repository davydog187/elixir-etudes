defmodule Teeth do

  def alert(lists) do
    alert(lists, 1, [])
  end

  defp alert([], _, a) do
    Enum.reverse(a)
  end

  defp alert([h|tail], current, a) do
    next = current + 1
    cond do
      Stats.maximum(h) >= 4 -> alert(tail, next, [current|a])
      True -> alert(tail, next, a)
    end
  end

